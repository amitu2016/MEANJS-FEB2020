
//Declaring a function
function helloWorld() {
    console.log("I am a named Function");
}


//calling a function
helloWorld();

let helloAgain = function() {
    console.log("I am Anonymous function assigned to a variable");
}

helloAgain();

let helloArrow = ()=> {
    console.log("I am a arrow function");
}

helloArrow();
